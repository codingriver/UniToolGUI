w('    {')
w('        var sb = new StringBuilder();')
w('        sb.Append("set theFile to (choose file name");')
w('        AppendPrompt(sb, title);')
w('        if (!string.IsNullOrEmpty(defaultFileName))')
w('            sb.Append(" default name \"" + EscAS(defaultFileName) + "\"" );')
w('        AppendDefaultLocation(sb, initialDir);')
w('        sb.Append(")\nreturn POSIX path of theFile");')
