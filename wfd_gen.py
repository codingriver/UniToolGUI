w('        return GetOpenFileName(ref ofn) ? ofn.lpstrFile : null;')
w('    }')
w('')
w('    public static string[] OpenFilePanelMulti(string title, string filter, string defaultExt = null, string initialDir = null)')
w('    {')
w('        var ofn = BuildOFN(filter, title, initialDir, defaultExt, MAX_PATH_MULTI,')
w('            OFN_FILEMUSTEXIST | OFN_HIDEREADONLY | OFN_NOCHANGEDIR | OFN_ALLOWMULTISELECT | OFN_EXPLORER);')
w('        if (!GetOpenFileName(ref ofn)) return null;')
