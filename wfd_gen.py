w('    private static string[] ParseExtensionsFromFilter(string filter)')
w('    {')
w('        if (string.IsNullOrEmpty(filter)) return null;')
w("        var parts = filter.Split(new[] { '\\0' }, StringSplitOptions.RemoveEmptyEntries);")
