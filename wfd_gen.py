w('        if (parts.Length == 0) return null;')
w('        if (parts.Length == 1) return parts;')
w('        var dir = parts[0];')
w('        var files = new string[parts.Length - 1];')
w('        for (int i = 1; i < parts.Length; i++) files[i - 1] = System.IO.Path.Combine(dir, parts[i]);')
w('        return files;')
w('    }')
w('')
w('    public static string SaveFilePanel(string title, string filter, string defaultExt = null, string initialDir = null, string defaultFileName = null)')
w('    {')
w('        var ofn = BuildOFN(filter, title, initialDir, defaultExt, MAX_PATH, OFN_OVERWRITEPROMPT | OFN_NOCHANGEDIR);')
w('        if (!string.IsNullOrEmpty(defaultFileName))')
w('        {')
w('            var buf = new char[MAX_PATH];')
w('            for (int i = 0; i < defaultFileName.Length && i < MAX_PATH - 1; i++) buf[i] = defaultFileName[i];')
w('            ofn.lpstrFile = new string(buf);')
w('        }')
w('        return GetSaveFileName(ref ofn) ? ofn.lpstrFile : null;')
w('    }')
w('')
w('    public static string OpenFolderPanel(string title, string initialDir = null)')
w('    {')
w('        var bi = new BROWSEINFOW')
w('        {')
w('            hwndOwner = GetActiveWindow(), lpszTitle = title,')
