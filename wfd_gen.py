w('public static class WindowsFileDialog')
w('{')
w('#if UNITY_STANDALONE_WIN || UNITY_EDITOR_WIN')
w('    #region Win32 常量与结构')
w('    private const int MAX_PATH        = 260;')
w('    private const int MAX_PATH_MULTI  = 65536;')
w('    private const int OFN_FILEMUSTEXIST    = 0x00001000;')
w('    private const int OFN_HIDEREADONLY     = 0x00000004;')
w('    private const int OFN_NOCHANGEDIR      = 0x00000008;')
w('    private const int OFN_OVERWRITEPROMPT  = 0x00000002;')
w('    private const int OFN_ALLOWMULTISELECT = 0x00000200;')
w('    private const int OFN_EXPLORER         = 0x00080000;')
w('    private const uint BIF_RETURNONLYFSDIRS = 0x0001;')
w('    private const uint BIF_NEWDIALOGSTYLE   = 0x0040;')
w('    private const uint BIF_EDITBOX          = 0x0010;')
