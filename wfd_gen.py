w('    private static void AppendPrompt(StringBuilder sb, string title)')
w('    {')
w('        if (!string.IsNullOrEmpty(title))')
w('            sb.Append(" with prompt \"" + EscAS(title) + "\"" );')
w('    }')
w('')
w('    private static void AppendDefaultLocation(StringBuilder sb, string dir)')
w('    {')
w('        if (!string.IsNullOrEmpty(dir))')
w(r'            sb.Append(" default location (POSIX file \"" + EscAS(dir.Replace("\\\\", "/")) + "\")");')
w('    }')
w('')
w('    private static void AppendExtensions(StringBuilder sb, string filter)')
w('    {')
w('        var exts = ParseExtensionsFromFilter(filter);')
w('        if (exts == null || exts.Length == 0) return;')
w('        sb.Append(" of type {");')
w('        for (int i = 0; i < exts.Length; i++)')
w('        {')
w('            if (i > 0) sb.Append(", ");')
w('            sb.Append("\"" + EscAS(exts[i]) + "\"" );')
w('        }')
w('        sb.Append("}");')
w('    }')
w('')
w('    /// <summary>')
w('    /// 通过 stdin 将 AppleScript 传给 osascript（osascript -）。')
w('    /// 无需写临时 .scpt 文件，无磁盘IO也无竞态风险。')
w('    /// </summary>')
w('    private static string RunOsascriptInline(string script)')
w('    {')
w('        try')
w('        {')
w('            var psi = new ProcessStartInfo')
w('            {')
w('                FileName = "osascript",')
w('                Arguments = "-",')
w('                UseShellExecute = false,')
w('                CreateNoWindow = true,')
w('                RedirectStandardInput  = true,')
w('                RedirectStandardOutput = true,')
w('                RedirectStandardError  = true,')
w('                StandardInputEncoding  = System.Text.Encoding.UTF8,')
w('                StandardOutputEncoding = System.Text.Encoding.UTF8,')
w('            };')
w('            using (var p = new Process { StartInfo = psi })')
w('            {')
w('                p.Start();')
