w('    {')
w('        if (filterPairs == null || filterPairs.Length % 2 != 0)')
w('            throw new ArgumentException("必须提供成对的描述和模式");')
w('        var sb = new StringBuilder();')
w(r"        for (int i = 0; i < filterPairs.Length; i += 2) { sb.Append(filterPairs[i]); sb.Append('\0'); sb.Append(filterPairs[i + 1]); sb.Append('\0'); }")
w(r"        sb.Append('\0');")
w('        return sb.ToString();')
w('    }')
