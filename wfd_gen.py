w('    private struct BROWSEINFOW')
w('    {')
w('        public IntPtr hwndOwner;')
w('        public IntPtr pidlRoot;')
w('        public string pszDisplayName;')
w('        public string lpszTitle;')
w('        public uint   ulFlags;')
w('        public IntPtr lpfn;')
w('        public IntPtr lParam;')
w('        public int    iImage;')
w('    }')
w('    #endregion')
w('    #region Win32 导入')
w('    [DllImport("comdlg32.dll", CharSet = CharSet.Auto, SetLastError = true)]')
w('    private static extern bool GetOpenFileName(ref OPENFILENAME ofn);')
